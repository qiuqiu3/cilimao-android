package cn.sddman.bt.mvp.e;


public class MagnetRule{
    private String source;
    private String site;
    private String waiting;

    private String group;
    private String magnet;
    private String name;
    private String size;
    private String url;
    private String count;
    private String hotsort;
    private String datesort;
    private String sizesort;
    private String hot;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public String getMagnet() {
        return magnet;
    }

    public void setMagnet(String magnet) {
        this.magnet = magnet;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getSite() {
        return site;
    }

    public void setSite(String site) {
        this.site = site;
    }

    public String getWaiting() {
        return waiting;
    }

    public void setWaiting(String waiting) {
        this.waiting = waiting;
    }

    public String getHotsort() {
        return hotsort;
    }

    public void setHotsort(String hotsort) {
        this.hotsort = hotsort;
    }

    public String getDatesort() {
        return datesort;
    }

    public void setDatesort(String datesort) {
        this.datesort = datesort;
    }

    public String getSizesort() {
        return sizesort;
    }

    public void setSizesort(String sizesort) {
        this.sizesort = sizesort;
    }

    public String getHot() {
        return hot;
    }

    public void setHot(String hot) {
        this.hot = hot;
    }
}
